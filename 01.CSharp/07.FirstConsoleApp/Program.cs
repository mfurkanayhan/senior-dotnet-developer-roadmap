// Prompt the user to enter their name.
Console.WriteLine("Please enter your name:");

// Read the name entered by the user using the Console.ReadLine() method and assign it to the 'name' variable.
string name = Console.ReadLine();

// Display a welcome message to the user. The 'name' variable is used here to include the user's name.
Console.WriteLine("Welcome, " + name + " (-_-)");

// Wait for the user to see the message by using Console.ReadLine() before the program exits.
Console.ReadLine();
